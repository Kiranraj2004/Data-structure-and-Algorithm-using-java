package A2Z_Strivers_DSA.bit_manupulation;

public class some_basic_operation {
    public static void main(String[] args) {
        int a=13;
        int b=5;
//        AND operator (&)
        System.out.println(a&b);
//        OR operator (|)
        System.out.println(a|b);
//        XOR operator (^)
        System.out.println(a^b);
//        NOT operator (~)
        System.out.println(~a);
        System.out.println(~-12);
//   largest number that int can store 2147483647 2power31-1
//   System.out.println();
        int s=Integer.MAX_VALUE;
        System.out.println(s);
//        smallest number that integer contains -2147483648 -2power31
        System.out.println(Integer.MIN_VALUE);
    }
}
